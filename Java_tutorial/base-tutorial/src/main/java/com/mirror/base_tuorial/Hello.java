/**
 * 这里是文档注释
 * 学会使用这个，
 * @author mirror
 */
public class Hello {//类名是Hello

    /*
    需要注意的是，一个Java文件中只允许一个公共类，并且公公类的类名要和文件名一致
     */
    public static void main(String[] args) {
        System.out.println("Hello,world!");
    }
    //上面叫方法，method。分辨方法和类的区别就是看有没有入参和返回类型
    /*
    为什么会强调这些，主要原因就是后续复杂了，还有静态方法之类的，
    那是真的难看懂
     */
}